//comparação
console.log(2 == 2); //dois sinais de iguais

//comparação rigorosa
console.log(2 === 2); //três sinais de iguais: aqui ele compara o número e o tipo de dado

//outros tipos de comparação: < > <= >= - eles são sempre booleanos

//comparacao de diferenca
console.log(10 != 10);

//console de diferença
console.log(10 !== "10");

//operador or(ou) ||
//operador and &&
//operador de negacao !

//comparador and (&&)
console.log(10 > 5 && 8 > 3); //o verdadeiro é sempre quando as duas afirmacoes sejam verdadeiras.

console.log(10 > 5 && 8 > 9); //o verdadeiro é sempre quando as duas afirmacoes sejam verdadeiras.

//comparador or ||
console.log(10 > 5 || 8 > 9); //o verdadeiro aqui é dada quando uma delas é verdadeiro.

//comparador de negacao !
console.log(!5 > 3); 
//aqui estou negando a afirmação, ou seja, numa afirmação verdadeira ele se torna falso; assim como o falso se torna verdadeiro.

